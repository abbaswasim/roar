#include "rorskeleton_data.h"

namespace ror
{
SkeletonData::~SkeletonData(void)
{
	for (uint32_t i = 0; i < m_no_of_bones; i++)
	{
		rDELETE(m_bones[i]);
	}

	rDELETEARRAY(m_bones);
	rDELETE(m_bind_shape_matrix);
}

}        // namespace ror
